package worker;
/**
 * A class that extends HourlyWorker and supplies a constructor for a Volunteer
 * 
 * @author Jaime Vega and Will Haanen
 * @version January 29, 2018
 */
public class Volunteer extends HourlyWorker
{
  /**
   * Creates a worker whose type is volunteer
   */
  public Volunteer(String newName)
  {
    super(newName);
  }
  
    public Volunteer(String newName, double hoursWorked)
  {
    super(newName, hoursWorked);
  }
  /**
   * Adds what type of worker they are to the front of the name
   * 
   * @return the name with the apporiate prefix
   */
  public String toString()
  {
   return "Volunteer"; 
  }
}